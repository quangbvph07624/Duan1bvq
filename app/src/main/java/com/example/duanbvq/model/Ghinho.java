package com.example.duanbvq.model;

public class Ghinho {
   public String name;
   public String mota;

    public Ghinho() {
    }

    public Ghinho(String name, String mota) {
        this.name = name;
        this.mota = mota;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }
}

